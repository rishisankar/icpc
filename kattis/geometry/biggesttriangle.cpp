template<class T>
struct Point {
	typedef Point P;
	T x, y;
	explicit Point(T x=0, T y=0) : x(x), y(y) {}
	bool operator<(P p) const { return tie(x,y) < tie(p.x,p.y); }
	bool operator==(P p) const { return tie(x,y)==tie(p.x,p.y); }
	P operator+(P p) const { return P(x+p.x, y+p.y); }
	P operator-(P p) const { return P(x-p.x, y-p.y); }
	P operator*(T d) const { return P(x*d, y*d); }
	P operator/(T d) const { return P(x/d, y/d); }
	T dot(P p) const { return x*p.x + y*p.y; }
	T cross(P p) const { return x*p.y - y*p.x; }
	T cross(P a, P b) const { return (a-*this).cross(b-*this); }
	T dist2() const { return x*x + y*y; }
	double dist() const { return sqrt((double)dist2()); }
	// angle to x-axis in interval [-pi, pi]
	double angle() const { return atan2(y, x); }
	P unit() const { return *this/dist(); } // makes dist()=1
	P perp() const { return P(-y, x); } // rotates +90 degrees
	P normal() const { return perp().unit(); }
	// returns point rotated 'a' radians ccw around the origin
	P rotate(double a) const {
		return P(x*cos(a)-y*sin(a),x*sin(a)+y*cos(a)); }
	friend ostream& operator<<(ostream& os, P p) {
		return os << "(" << p.x << "," << p.y << ")"; }
};

template<class P>
pair<int, P> lineInter(P s1, P e1, P s2, P e2) {
	auto d = (e1 - s1).cross(e2 - s2);
	if (d == 0) // if parallel
		return {-(s1.cross(e1, s2) == 0), P(0, 0)};
	auto p = s2.cross(e1, e2), q = s2.cross(e2, s1);
	return {1, (s1 * p + e1 * q) / d};
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cin.exceptions(cin.failbit);
    cout.setf(ios::fixed);
    cout.precision(10);

    int n;
    cin >> n;

    vector<pair<Point<ld>, Point<ld>>> pts(n);
    REP(i, n) {
        cin >> pts[i].first.x >> pts[i].first.y >> pts[i].second.x >> pts[i].second.y;
    }

    ld r = -1;

    for (pair<Point<ld>, Point<ld>> pair1 : pts) {
        for (pair<Point<ld>, Point<ld>> pair2 : pts) {
            for (pair<Point<ld>, Point<ld>> pair3 : pts) {
                pair<int, Point<ld>> r1 = lineInter(pair1.first, pair1.second, pair2.first, pair2.second);
                pair<int, Point<ld>> r2 = lineInter(pair1.first, pair1.second, pair3.first, pair3.second);
                pair<int, Point<ld>> r3 = lineInter(pair2.first, pair2.second, pair3.first, pair3.second);
                if (r1.first != 1 || r2.first != 1 || r3.first != 1) continue;
                if (r1.second == r2.second || r1.second == r3.second || r2.second == r3.second) continue;
                ld res = (r1.second - r2.second).dist() + (r2.second - r3.second).dist() + (r3.second - r1.second).dist();
                r = max(r, res);
            }
        }
    }

    if (r == -1) cout << "no triangle\n";
    else cout << r << '\n';


}