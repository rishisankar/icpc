void run() {
    int n,k; cin >> n >> k;
    string s; cin >> s;

    if (k == 0) {
        print(1);
        return;
    }

    ll sm = 0;
    bool added = false;

    int ones = 0;
    int j = n-1;
    int previ = -1;
    for (int i = n-1; i >= 0; --i) {
        if (s[i] == '1') ++ones;
        if (ones >= k && (i == 0 || s[i-1] == '1')) {
            while (ones > k && j >= i) {
                if (s[j] == '1') --ones;
                --j;
            }

            if (i <= j) {

                // can process
                ll sz = j-i+1;

                added = true;
                sm += nCrModPFermat(sz,k,mod);

                if (previ != -1) {
                    // subtract.
                    ll sz2 = j - previ + 1;
                    sm -= nCrModPFermat(sz2, k-1, mod);
                }
                sm+=mod;
                sm%=mod;
                previ = i;


            }

        }
    }

    if (!added) print(1);
    else print(sm);
}

